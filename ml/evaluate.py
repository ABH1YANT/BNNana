def generate_markdown_report(metrics, path):
    """Writes a human-readable summary of the model performance."""
    cm = metrics['confusion_matrix']
    report = f"""# Training Evaluation Report

## Model Performance Metrics
- **Accuracy:** {metrics['accuracy']:.4f}
- **Precision:** {metrics['precision']:.4f}
- **Recall:** {metrics['recall']:.4f}
- **F1-Score:** {metrics['f1_score']:.4f}

## Confusion Matrix
| | Predicted Benign | Predicted DDoS |
|---|---|---|
| **Actual Benign** | {cm[0][0]} | {cm[0][1]} |
| **Actual DDoS** | {cm[1][0]} | {cm[1][1]} |

---
*Generated by BWN Accelerator ML Workstream*
"""
    with open(path, "w") as f:
        f.write(report)

